<h2>Poileasaidh</h2>

<p>1) Le bhith a’ cleachdadh ar làrach-lìn agus ag aontachadh ris a’ phoileasaidh seo, tha thu a’ toirt cead dhuinn ar criomagan a chleachdadh a rèir teirmean a’ phoileasaidh seo</p>

<p>2) Dàta pearsanta<br />
2.1 - Cha bhi sinn a’ cruthachadh agus a’ cumail an “dàta pearsanta agad”<br />
2.2 - Mar sin, cha bhi sinn a’ toirt seachad do dhàta pearsanta do dhaoine eile

<p>3) Cànan<br />

3.1 - ‘’S e Gàidhlig na h-Alba am prìomh chànan a bhios sinn a’ cleachdadh air an làrach-lìn. Bidh sinn a’ cleachdadh a’ Ghàidhlig agus/no Beurla gus ceistean mu phriobhaideachd a fhreagairt, ge-tà, nuair a bhios tionndadh feumail. 
</p>

</p>4) Atharrachaidhean<br />
4.1 - Faodaidh sinn am poileasaidh seo ùrachadh bho àm gu àm le bhith a’ foillseachadh dreach ùr air an làrach-lìn againn.<br />
4.2 - Mar sinn, bu chòir dhut sùil a thoirt air an duilleag seo bho àm gu àm.</p>

<p>5) Làraich-lìn treas pàrtaidh<br />
5.1 - Tha an làrach-lìn againn a’ toirt a-steach ceanglaichean gu làraichean-lìn treas-phàrtaidh.<br />
5.2 - Chan eil smachd againn air poileasaidhean dìomhaireachd treas-phàrtaidhean.</p>

<p>6) Criomagan<br />
6.1 - Bidh sinn a’ cleachdadh criomagan foincseanach, gus am bi an làrach-lìn ag obair gu ceart, agus criomagan airson staitisdearachd.<br />
